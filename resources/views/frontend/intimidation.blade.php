                                <h3>La cible </h3>
                                <p>La cible représente la personne visée par l'intimidation.   
                                    Pourquoi parler de cible plutôt que de victime?   
                                    Cela permet de faire référence aux personnes ciblées par l'intimidation sans leur faire porter
                                    l'étiquette de victimes. Certaines personnes ciblées par l'intimidation peuvent s'affirment face à la
                                    personne qui les agresse, certaines personnes réussissent à conserver un équilibre dans la
                                    répartition des pouvoirs avec la personne qui les intimide et certaines personnes n'ont
                                    simplement pas le sentiment d'être des victimes. Comme chez la personne qui intimide,
                                    l'imposition d'une étiquette peut altérer la perception qu'une personne a de soi et peut réduire
                                    son sentiment d'agentivité dans une situation donnée. Les incidences d'intimidation sont
                                    différentes pour chaque individu·e. Parler de cible évite les suppositions quant à l'expérience
                                    singulière des personnes vivant de l'intimidation.
                                    Bien que tou·tes puissent être la cible d'intimidation à un moment ou un autre de sa vie, les
                                    personnes issues de minorités ou étant marginalisées par la société seraient plus sujettes à vivre
                                    de l'intimidation en raison de leurs facteurs de vulnérabilité spécifiques, notamment les
