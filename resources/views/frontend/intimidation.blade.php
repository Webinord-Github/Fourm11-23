                                <h3>La personne qui intimide</h3>
                                <p>La personne qui intimide est la personne qui initie une action d'intimidation auprès d'une cible.
                                    Elle utilise la violence à répétition pour prendre le pouvoir sur sa cible et pour nuire ou amoindrir
                                    cette dernière.
                                    <br><br>
                                    La personne qui intimide peut avoir diverses motivations à poser de tels comportements, telles
                                    que le désir d'attirer l'attention, de se sentir supérieure, de se venger ou de se défendre. La
                                    personne qui intimide a souvent une faible estime d'elle-même, bien qu'elle puisse parfois donner
                                    l'impression d'avoir une grande confiance en soi. Quelle qu'en soit la raison, l'intimidation n'est
                                    jamais justifiable ni acceptable.
                                    <br><br>
                                    Divers facteurs peuvent influencer une personne à poser des gestes d'intimidation ; son
                                    environnement familial, une exposition précoce à la violence, un manque d'empathie, un
                                    historique d'intimidation vécue, etc. Chaque situation d'intimidation doit être analysée selon les
                                    facteurs individuels des personnes impliquées. <br><br>

                                    Pourquoi parler de personne qui intimide et pas d'intimidateur·rice?<br><br>

                                    Lorsque l'on utilise ce terme, c'est le comportement d'intimidation qui est ciblé et non la personne
                                    elle-même. Utiliser un vocabulaire centré sur les comportements problématiques permet d'éviter
                                    de poser une étiquette indélébile pouvant mener une personne à intensifier ses comportements
                                    intimidateurs pour incarner l'étiquette apposée. Il est souhaitable d'intervenir sur les
                                    comportements problématiques posés et de mettre au clair pourquoi ces derniers sont
