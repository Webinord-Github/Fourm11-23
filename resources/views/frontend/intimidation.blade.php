                                <h3>La personne qui intimide</h3>
                                <p>Il est souhaitable que les témoins interviennent rapidement lorsqu'une situation d'intimidation se
                                    déroule devant leurs yeux, en s'assurant de respecter leurs limites et leur propre sécurité.</p>
                                <p>Quelques recommandations d'intervention pour les témoins actif·ves :</p>
                                <li>Mettre au clair, avec la personne qui intimide, que les comportements violents sont
                                    inacceptables et qu'ils doivent prendre fin immédiatement.</li>
                                <li>Nommer les comportements posés et les types de violence associés.</li>
                                <li>Nommer les comportements attendus ou de réparation par la personne qui intimide.</li>
                                <p>Il est également possible de documenter la situation (qui, quoi, où, quand, comment) et de rester
                                    attentif∙ves aux interactions entre les personnes concernées par la situation pour éviter une
                                    escalade de la violence. Si les personnes impliquées sont des mineur∙es, il est suggéré de tenir
                                    les adultes qui en sont responsable au fait des démarches.</p>
                                <h3>La cible</h3>
                                <p>Comme les actes d'intimidation sont causés par les personnes qui intimident, il importe d'éviter
                                    de faire reposer la responsabilité de l'intimidation sur la cible. Identifier les comportements que la
                                    cible aurait dû adopter pour éviter ou faire cesser l'incidence n'est pas une intervention
                                    recommandée dans l'immédiat, comme elle peut provoquer un sentiment de culpabilité et
                                    décupler le poids des conséquences déjà bien présentes chez cette dernière. Ces stratégies
                                    peuvent faire l'objet d'un plan d'intervention global à moyen et long terme en fonction des besoins
                                    de l'individu·e. Ce sont les personnes qui intimident qui doivent appliquer des changements à
                                    leurs comportements.</p>
                                <p>Dans le cas d'un dévoilement d'une situation d'intimidation, il est souhaitable de/d' :</p>
                                <li>Croire spontanément les personnes ciblées par la violence.</li>
                                <li>S'adresser à la cible, s'assurer de son état, l'écouter et l'épauler au besoin.</li>
                                <li>Intervenir auprès de tout le milieu, en favorisant une culture d'entraide, d&#39;empathie, de
                                    bienveillance et un sentiment d'appartenance.</li>
                                <li>Questionner la cible pour connaitre les faits :</li>
                                <ul>
                                    <li class="custom-li">Qui est la personne initiatrice de l'intimidation?</li>
                                    <li class="custom-li">Qui sont les témoins?</li>
                                    <li class="custom-li">Quels actes ont été posés?</li>
                                    <li class="custom-li">Quand la problématique a-t-elle débuté?</li>
                                    <li class="custom-li">Est-ce que l'intimidation se vit aussi en ligne (cyberintimidation)?</li>
                                </ul>
                                <p>Il est recommandé de réconforter la personne ciblée par la violence, de valider son expérience et
                                    ses sentiments, puisque le soutien perçu peut contribuer à réduire les conséquences de
                                    l'intimidation chez elle.</p>
                                <p>Il importe de rester vigilant∙e et d'assurer une supervision aux moments et dans les lieux où
                                    l'intimidation peut avoir lieu (dans les vestiaires, près des casiers, entre les cours, sur les réseaux
                                    sociaux, etc.). Si des mineur∙es sont concerné∙es par une incidence d'intimidation, il est important
                                    de tenir leurs adultes responsables au fait de la situation et des interventions entreprises par le
