                                <h3>Répétition</h3>
                                <p>La répétition de la violence par des gestes, des paroles ou des attitudes est susceptible de créer
                                    un déséquilibre des pouvoirs entre les personnes qui la perpétuent et les personnes qui en sont
                                    la cible. La répétition peut se faire par un·e individu∙e ou peut être le résultat de violences
                                    perpétrées par plusieurs personnes à des moments différents sur une même personne cible.  Le
                                    caractère répétitif permet de différencier une agression d'une situation d'intimidation. Une
                                    agression relève d'un événement isolé de violence, tandis que l'intimidation comprend la
                                    répétition d'agissements violents envers une cible à travers le temps. Dans le cas de la
                                    cyberintimidation, la répétition peut prendre la forme du nombre de partages d'une publication, du
                                    nombre de commentaires désobligeants ou moqueurs sur cette dernière, du nombre de
                                    personnes qui « aiment » la publication, etc.</p>
